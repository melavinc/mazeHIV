export const CELL_TYPES = {
  PATH: 0,
  WALL: 1,
  VIRUS: 2,
  START: 3,
  GOAL: 4,
  CONDOM: 5,
};

export function getStartPosition(grid) {
  for (let r = 0; r < grid.length; r++) {
    for (let c = 0; c < grid[r].length; c++) {
      if (grid[r][c] === CELL_TYPES.START) return { row: r, col: c };
    }
  }
  return { row: 1, col: 0 };
}

export function getGoalPosition(grid) {
  for (let r = 0; r < grid.length; r++) {
    for (let c = 0; c < grid[r].length; c++) {
      if (grid[r][c] === CELL_TYPES.GOAL) return { row: r, col: c };
    }
  }
  return { row: 13, col: 20 };
}

export function getVirusPositions(grid) {
  const positions = [];
  for (let r = 0; r < grid.length; r++) {
    for (let c = 0; c < grid[r].length; c++) {
      if (grid[r][c] === CELL_TYPES.VIRUS) positions.push({ row: r, col: c });
    }
  }
  return positions;
}

export function getCondomPositions(grid) {
  const positions = [];
  for (let r = 0; r < grid.length; r++) {
    for (let c = 0; c < grid[r].length; c++) {
      if (grid[r][c] === CELL_TYPES.CONDOM) positions.push({ row: r, col: c });
    }
  }
  return positions;
}